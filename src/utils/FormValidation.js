export const isEmailValid = (value) => {
  // eslint-disable-next-line
  // const ignoredCharsReg = /[!#$%^&*()+\-=\[\]{};':"\\|,<>\/?]+/;
  const ignoredCharsReg = /[!#$%^&*()=[]{};':"\\|,<>\/?]+/;
  return !!value && value.search(ignoredCharsReg) === -1 && yupREmail.test(value);
}

